#include "sort.h"    // Подключаем заголовочный файл, содержащий определения структуры и таблицы
#include <iostream>  // Подключаем стандартную библиотеку для ввода-вывода (std::cout и т.д.)

// Функция для инициализации таблицы значениями по умолчанию
void initializeTable() {
    // Перебираем все элементы двумерного массива m размером (MAXLEN+1) x (MAXLEN+1)
    for (int i = 0; i <= MAXLEN; i++) {
        for (int j = 0; j <= MAXLEN; j++) {
            m[i][j].cost = 0;      // Устанавливаем стоимость для текущей ячейки равной 0
            m[i][j].parent = -1;   // Устанавливаем "родителя" текущей ячейки равным -1 (нет родителя)
        }
    }
}

// Функция для вывода содержимого таблицы на экран
void printTable() {
    // Перебираем все элементы двумерного массива m
    for (int i = 0; i <= MAXLEN; i++) {
        for (int j = 0; j <= MAXLEN; j++) {
            // Выводим содержимое текущей ячейки: cost (стоимость) и parent (родительская ячейка)
            std::cout << "m[" << i << "][" << j << "] = { cost: "
                << m[i][j].cost << ", parent: " << m[i][j].parent << " }" << std::endl;
        }
    }
}

// Главная функция программы
int main() {
    // Инициализация таблицы значениями по умолчанию
    initializeTable();

    // Вывод сообщения о начале таблицы
    std::cout << "Dynamic Programming Table:" << std::endl;

    // Печать содержимого таблицы на экран
    printTable();

    // Завершаем выполнение программы
    return 0;
}